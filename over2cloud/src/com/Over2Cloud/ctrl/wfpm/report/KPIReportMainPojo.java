package com.Over2Cloud.ctrl.wfpm.report;

public class KPIReportMainPojo
{
	private int id;
	private String achieved;
	private String achievedPercentage;
	private String remaining;
	private String remainingPercentage;
	private String incentive;
	private String month;
	
	public int getId()
	{
		return id;
	}
	public void setId(int id)
	{
		this.id = id;
	}
	public String getAchieved()
	{
		return achieved;
	}
	public void setAchieved(String achieved)
	{
		this.achieved = achieved;
	}
	public String getAchievedPercentage()
	{
		return achievedPercentage;
	}
	public void setAchievedPercentage(String achievedPercentage)
	{
		this.achievedPercentage = achievedPercentage;
	}
	public String getRemaining()
	{
		return remaining;
	}
	public void setRemaining(String remaining)
	{
		this.remaining = remaining;
	}
	public String getRemainingPercentage()
	{
		return remainingPercentage;
	}
	public void setRemainingPercentage(String remainingPercentage)
	{
		this.remainingPercentage = remainingPercentage;
	}
	public String getIncentive()
	{
		return incentive;
	}
	public void setIncentive(String incentive)
	{
		this.incentive = incentive;
	}
	public String getMonth()
	{
		return month;
	}
	public void setMonth(String month)
	{
		this.month = month;
	}
	
	
}
