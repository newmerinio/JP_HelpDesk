package com.Over2Cloud.ctrl.group;

public class AllgroupView {

	private int id;
	private String grpName;
	private String grpDesc;
	private String memberShipType;
	private String userName;
	private int totalMember;
	private String date;
	private String time;
	private String userTrue="false";
	private String grpLevel="Private";
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getGrpName() {
		return grpName;
	}
	public void setGrpName(String grpName) {
		this.grpName = grpName;
	}
	public String getGrpDesc() {
		return grpDesc;
	}
	public void setGrpDesc(String grpDesc) {
		this.grpDesc = grpDesc;
	}
	public String getMemberShipType() {
		return memberShipType;
	}
	public void setMemberShipType(String memberShipType) {
		this.memberShipType = memberShipType;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public int getTotalMember() {
		return totalMember;
	}
	public void setTotalMember(int totalMember) {
		this.totalMember = totalMember;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getUserTrue() {
		return userTrue;
	}
	public void setUserTrue(String userTrue) {
		this.userTrue = userTrue;
	}
	public String getGrpLevel() {
		return grpLevel;
	}
	public void setGrpLevel(String grpLevel) {
		this.grpLevel = grpLevel;
	}
	
	
}
