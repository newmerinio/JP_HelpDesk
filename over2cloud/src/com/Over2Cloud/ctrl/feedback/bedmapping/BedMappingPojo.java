package com.Over2Cloud.ctrl.feedback.bedmapping;

public class BedMappingPojo {
	private int id;
	private String emp_contact_id;
	private String bed_name;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getEmp_contact_id() {
		return emp_contact_id;
	}
	public void setEmp_contact_id(String emp_contact_id) {
		this.emp_contact_id = emp_contact_id;
	}
	public String getBed_name() {
		return bed_name;
	}
	public void setBed_name(String bed_name) {
		this.bed_name = bed_name;
	}

}
