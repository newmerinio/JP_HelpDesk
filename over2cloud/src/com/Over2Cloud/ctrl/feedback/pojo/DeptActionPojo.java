package com.Over2Cloud.ctrl.feedback.pojo;

public class DeptActionPojo
{
	private String deptId;
	private String deptName;
	private String totalIsues;
	private String totalAction;
	private String totalCapa;
	private String percent;
	public String getDeptId()
	{
		return deptId;
	}
	public void setDeptId(String deptId)
	{
		this.deptId = deptId;
	}
	public String getDeptName()
	{
		return deptName;
	}
	public void setDeptName(String deptName)
	{
		this.deptName = deptName;
	}
	public String getTotalIsues()
	{
		return totalIsues;
	}
	public void setTotalIsues(String totalIsues)
	{
		this.totalIsues = totalIsues;
	}
	public String getTotalAction()
	{
		return totalAction;
	}
	public void setTotalAction(String totalAction)
	{
		this.totalAction = totalAction;
	}
	public String getTotalCapa()
	{
		return totalCapa;
	}
	public void setTotalCapa(String totalCapa)
	{
		this.totalCapa = totalCapa;
	}
	public String getPercent() {
		return percent;
	}
	public void setPercent(String percent) {
		this.percent = percent;
	}
	
}
