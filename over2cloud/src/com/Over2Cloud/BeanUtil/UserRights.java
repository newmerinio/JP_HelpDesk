package com.Over2Cloud.BeanUtil;

public class UserRights 
{
	private int id;
	private String rightsName;
	private String rightsHeading;
	private String add;
	private String view;
	private String modify;
	private String delete;
	private String moduleAbbreviation;
	private String moduleHeading;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getRightsName() {
		return rightsName;
	}
	public void setRightsName(String rightsName) {
		this.rightsName = rightsName;
	}
	public String getRightsHeading() {
		return rightsHeading;
	}
	public void setRightsHeading(String rightsHeading) {
		this.rightsHeading = rightsHeading;
	}
	public String getAdd() {
		return add;
	}
	public void setAdd(String add) {
		this.add = add;
	}
	public String getView() {
		return view;
	}
	public void setView(String view) {
		this.view = view;
	}
	public String getModify() {
		return modify;
	}
	public void setModify(String modify) {
		this.modify = modify;
	}
	public String getDelete() {
		return delete;
	}
	public void setDelete(String delete) {
		this.delete = delete;
	}
	public String getModuleAbbreviation() {
		return moduleAbbreviation;
	}
	public void setModuleAbbreviation(String moduleAbbreviation) {
		this.moduleAbbreviation = moduleAbbreviation;
	}
	public String getModuleHeading() {
		return moduleHeading;
	}
	public void setModuleHeading(String moduleHeading) {
		this.moduleHeading = moduleHeading;
	}
	
}




